<h1>How to get Google Places API key </h1>
<div>
    <ol>
        <li>
            Go to your <a href="https://console.developers.google.com/apis/dashboard?pli=1" target="_blank">Google
                Console dashboard</a>. If you new user agree Google terms:
        </li>
        <li>Select your existing project or create new:</li>
        <li>Wait for creation and then click 'ENABLE APIS AND SERVICES':</li>
        <li>Type 'Places API' in the search area, select the first result 'Places API' and click 'ENABLE' button:</li>
        <li>After enable the Places API, click 'Navigation menu', select 'APIs & Services' and 'Credentials':</li>
        <li>On Credentials page click '+ CREATE CREDENTIALS' and select 'API key':</li>
        <li>After creation of API key you need to copy & paste it to the plugin's settings (please do not restrict the
            key, the plugin can't work with a restricted API key, it's a limitation of Google):
        </li>
        <li> Now, you need to enable Google Billing. It's mandatory step to use the API key. Donâ€™t worry, Google is
            currently giving $200 free credit a month and it should be enough to use the plugin for connecting several
            Google places without additional <a href="https://developers.google.com/maps/billing-credits" target="_blank"> fees except this free $200 credits</a>. Go to <a
                    href="https://console.cloud.google.com/project/_/billing/enable" target="_blank">https://console.cloud.google.com/project/_/billing/enable</a>,
            select your recent created project and click 'CREATE BILLING ACCOUNT':
        </li>
        <li>Agree Google terms, fill your contact information and start using your Google API key with the plugin.</li>
    </ol>

